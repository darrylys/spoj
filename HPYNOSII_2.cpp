int getval() {
    char x; char arr[10]; int cntr=0; int mul=1; int total=0;
    while ((x=getchar())!='\n' && x!=EOF) {
        arr[cntr++] = x;
    }
    while (cntr--) {
        total+=(arr[cntr]-'0')*mul;
        mul*=10;
    }
    return total;
}

int main() {
    int t,n,tmp;
    t = getval();
    //printf("t=%d\n", t);
    while (t--) {
        n=getval();
        //printf("n=%d\n", n);
        tmp=0;
        while (n>0) {
            tmp += (n%10)*(n%10);
            n/=10;
        }
        if (tmp == 1) putchar('1');
        else {
            if (flag[tmp-1] == -1) {
                putchar('-');
                putchar('1');
            } else {
                putchar((char)flag[tmp-1]+'1');
            }
        }
        putchar('\n');
    }
}
